
class Person{
	String name;
	int age;
}

public class UsingClasses {

	public static void main(String[] args) {

		Person Person1 = new Person();
		Person1.name = "Sneha";
		Person1.age = 28;
		
		Person Person2 = new Person();
		Person2.name = "Asmi";
		Person2.age = 2;
		
		System.out.println(Person2.name);
	}

}
